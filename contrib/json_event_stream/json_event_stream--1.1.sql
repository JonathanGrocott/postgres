CREATE FUNCTION json_event_stream_set_retention(
	stream_table regclass,
	retention interval
)
RETURNS void
LANGUAGE plpgsql
AS $$
BEGIN
	PERFORM json_event_stream_require_registered(stream_table);
	PERFORM json_event_stream_require_owner(stream_table);

	UPDATE json_event_streams
	SET retention = json_event_stream_set_retention.retention
	WHERE json_event_streams.stream_table = json_event_stream_set_retention.stream_table;
END;
$$;

CREATE FUNCTION json_event_stream_apply_retention(
	stream_table regclass DEFAULT NULL,
	dry_run boolean DEFAULT true
)
RETURNS TABLE(stream regclass, partition regclass, partition_to timestamptz, would_drop boolean)
LANGUAGE plpgsql
AS $$
DECLARE
	part record;
BEGIN
	IF stream_table IS NOT NULL THEN
		PERFORM json_event_stream_require_registered(stream_table);
		PERFORM json_event_stream_require_owner(stream_table);
	END IF;

	FOR part IN
		SELECT
			s.stream_table,
			child.oid::regclass AS partition_table,
			pg_get_expr(child.relpartbound, child.oid)::text AS part_bound,
			s.retention
		FROM json_event_streams s
		JOIN pg_inherits i ON i.inhparent = s.stream_table
		JOIN pg_class child ON child.oid = i.inhrelid
		WHERE s.retention IS NOT NULL
		  AND (json_event_stream_apply_retention.stream_table IS NULL
			   OR s.stream_table = json_event_stream_apply_retention.stream_table)
	LOOP
		PERFORM json_event_stream_require_owner(part.stream_table);

		partition_to := substring(part.part_bound FROM 'TO \(''([^'']+)''\)')::timestamptz;
		stream := part.stream_table;
		partition := part.partition_table;
		would_drop := partition_to < clock_timestamp() - part.retention;

		IF would_drop AND NOT dry_run THEN
			EXECUTE format('DROP TABLE %s', part.partition_table);
		END IF;

		RETURN NEXT;
	END LOOP;
END;
$$;

CREATE FUNCTION json_event_stream_add_payload_path_index(
	stream_table regclass,
	index_name name,
	json_path text
)
RETURNS void
LANGUAGE plpgsql
AS $$
BEGIN
	PERFORM json_event_stream_require_registered(stream_table);
	PERFORM json_event_stream_require_owner(stream_table);

	IF json_path IS NULL OR json_path = '' THEN
		RAISE EXCEPTION 'json_path must not be empty';
	END IF;

	EXECUTE format(
		'CREATE INDEX IF NOT EXISTS %I ON %s ((payload #>> %L))',
		index_name,
		stream_table,
		string_to_array(json_path, '.'));
END;
$$;

CREATE VIEW json_event_ingestion_status AS
SELECT
	src.source_name,
	src.source_type,
	src.stream_table,
	src.enabled,
	src.config,
	src.last_seen_at,
	src.last_error,
	clock_timestamp() - src.last_seen_at AS ingestion_lag
FROM json_event_sources src;

CREATE VIEW json_event_rejected_messages AS
SELECT
	dead_letter_id,
	stream_table,
	source,
	topic,
	received_at,
	error_code,
	error_message,
	headers,
	raw_payload
FROM json_event_dead_letters;

CREATE VIEW json_event_partition_status AS
SELECT
	s.stream_table,
	child.oid::regclass AS partition_table,
	pg_get_expr(child.relpartbound, child.oid) AS partition_bound,
	pg_total_relation_size(child.oid) AS total_bytes,
	s.retention
FROM json_event_streams s
JOIN pg_inherits i ON i.inhparent = s.stream_table
JOIN pg_class child ON child.oid = i.inhrelid;

CREATE VIEW json_event_index_recommendations AS
SELECT
	s.stream_table,
	CASE
		WHEN NOT s.payload_gin_enabled THEN 'Consider a payload GIN index for jsonpath predicates.'
		ELSE 'Payload GIN index requested for this stream.'
	END AS recommendation
FROM json_event_streams s;
